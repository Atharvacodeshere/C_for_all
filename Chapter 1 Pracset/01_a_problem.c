#include <stdio.h>

int main() {
    // Hardcoded values for length and width
    int length = 10;
    int width = 5;
    
    // Calculate area
    int area = length * width;
    
    // Print the result
    printf("The area of the rectangle with length %d and width %d is: %d\n", length, width, area);

    return 0;
}
